
public class Variable {

	public static void main(String[] args) {
		// Declare variable(변수를 선언)
		// Data type 선언		
		int score;
		// 변수의 리터럴 값을 입력
		// = 대입 ==&=!값의 비교		
		score = 100;
		//한줄 복사 ctrl+alt+밑 방향키
		System.out.println(score+1);
		System.out.println(score+2);
		score = score + 3;
	}
	
}
