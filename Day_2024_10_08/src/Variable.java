
public class Variable {

	public static void main(String[] args) {
		// Declare variable(변수를 선언)
		// Declare Data type(데이터 종류를 선언)
		int score;
		// 변수의 리터럴 값을 입력
		// = 대입 !=&== 값의 비교
		score = 100;
		// ctrl+alt+↑ 혹은 ctrl+alt+↓ : 한 줄 복사
		System.out.println(score + 1);
		System.out.println(score + 2);
		score = score + 3;
	}

}