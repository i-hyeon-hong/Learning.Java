
public class Variable2 {

	public static void main(String[] args) {
		// 변수 초기화(initial) : 선언과 동시에 값 입력
		int a = 100;
		System.out.println("a is :" + a);
		// a 보다 1 큰 b 변수
		int b = a + 1;
		System.out.println("b is :" + b);
		// i,j는 선언만 k는 34의 값을 가짐
		// int i,j,k = 34;
		//
		int i;
		int j;
		int k = 34;
		// 실수형 값
		double c = 3.14;
		System.out.println("c is :" + c);
	}

}