
public class Operator4 {

	public static void main(String[] args) {
		// TODO : 연산자의 대해 알아보기

		// 문자열 연결
		System.out.println(4 + 5);
		System.out.println("영구와 " + "땡칠이");
	
		// 문자열과 정수의 연결
		System.out.println("응답하라 " + 1989);
		System.out.println(1989 + " 응답하라");
		
		// 부호 연산자
		int number = -10;
		System.out.println(-number);
		System.out.println(+number);
	}

}
